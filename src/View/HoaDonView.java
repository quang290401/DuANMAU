    public HoaDon GetFrom() {
        KhachHang idKH = (KhachHang) CbbIdKH.getSelectedItem();
        String idKhachHang = idKH.getId();
        NhanVien idNV = (NhanVien) CbbIDNV.getSelectedItem();
        String idNhanVien = idNV.getId();
        String ma = txtMa.getText();
        String ngayTao = txtNgayTao.getText();
        String ngayThanhToan = txtNgThanhToan.getText();
        String ngaySip = txtNgShip.getText();
        String ngNhan = txtNgNhan.getText();
        String trangThai = txtTinhtrang.getText();
        String tenNgNhan = txtTenNgNhan.getText();
        String diaChi = txtDiaChi.getText();
        String soDt = txtSDT.getText();
        try {
            SimpleDateFormat simple = new SimpleDateFormat("dd-MM-yyyy");
            Date date = simple.parse(ngayTao);
            java.sql.Date sqlDate = new java.sql.Date(date.getTime());
            Date date2 = simple.parse(ngayThanhToan);
            java.sql.Date sqlDate2 = new java.sql.Date(date2.getTime());
            Date date3 = simple.parse(ngaySip);
            java.sql.Date sqlDate3 = new java.sql.Date(date3.getTime());
            Date date4 = simple.parse(ngNhan);
            java.sql.Date sqlDate4 = new java.sql.Date(date4.getTime());
            Integer tt = Integer.valueOf(trangThai);
            HoaDon hoaDon = new HoaDon(idKhachHang, idNhanVien, ma, sqlDate, sqlDate2, sqlDate3, sqlDate4, tt, tenNgNhan, diaChi, soDt);
            return hoaDon;
        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

