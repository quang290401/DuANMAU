    private void tblHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHoaDonMouseClicked
        int row = tblHoaDon.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = tblHoaDon.getValueAt(row, 0).toString();
        String ma = tblHoaDon.getValueAt(row, 3).toString();
        String ngaytao = tblHoaDon.getValueAt(row, 4).toString();
        String ngThanhToan = tblHoaDon.getValueAt(row, 5).toString();
        String ngShip = tblHoaDon.getValueAt(row, 6).toString();
        String ngNhan = tblHoaDon.getValueAt(row, 7).toString();
        String tinhTrang = tblHoaDon.getValueAt(row, 8).toString();
        String tenNgNhan = tblHoaDon.getValueAt(row, 9).toString();
        String DiaChi = tblHoaDon.getValueAt(row, 10).toString();
        String Std = tblHoaDon.getValueAt(row, 11).toString();

        this.txtID.setText(id);
        this.txtMa.setText(ma);

        this.txtNgayTao.setText(ngaytao);
        this.txtNgThanhToan.setText(ngThanhToan);
        this.txtNgShip.setText(ngShip);
        this.txtNgNhan.setText(ngNhan);
        this.txtTinhtrang.setText(tinhTrang);
        this.txtTenNgNhan.setText(tenNgNhan);
        this.txtDiaChi.setText(DiaChi);
        this.txtSDT.setText(Std);
        Object idCHObj = tblHoaDon.getValueAt(row, 1);
        Object idNvObj = tblHoaDon.getValueAt(row, 2);

// Kiểm tra và chuyển đổi thành chuỗi
        String idKH = (idCHObj != null) ? idCHObj.toString() : "";
        String idNV = (idNvObj != null) ? idNvObj.toString() : "";

// Đặt mô hình cho combobox CbbIdCh
        DefaultComboBoxModel<String> idKHModel = new DefaultComboBoxModel<>();
        idKHModel.addElement(idKH);
        CbbIdKH.setModel(idKHModel);

// Đặt mô hình cho combobox CbbIdCV
        DefaultComboBoxModel<String> idNVModel = new DefaultComboBoxModel<>();
        idNVModel.addElement(idNV);
        CbbIDNV.setModel(idNVModel);


    }//GEN-LAST:event_tblHoaDonMouseClicked

