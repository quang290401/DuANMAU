    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        try {
            String ma = txtMa.getText();
            String ten = txtTen.getText();
            String dem = txtTenDem.getText();
            String ho = txtHo.getText();
            String ngayS = txtNgaySinh.getText();
            String SDt = txtSdt.getText();
            String diaC = txtDiaChi.getText();
            String thanhP = txtThanhPho.getText();
            String quocGia = txtQuocGia.getText();
            String mk = txtMK.getText();
            String id = txtID.getText();
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd-MM-yyyy");
            Date date2 = simpleDateFormat.parse(ngayS);
            java.sql.Date sqldate = new java.sql.Date(date2.getTime());
            service.UpdateKH(ma, ten, dem, ho, sqldate, SDt, diaC, thanhP, quocGia, mk, id);
            this.LoadTable();
        } catch (ParseException ex) {
            ex.printStackTrace();
    }//GEN-LAST:event_btnSuaActionPerformed
    }

