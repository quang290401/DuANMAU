    public void LoadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblKhachHang.getModel();
        ArrayList<KhachHangViewModels> khachHangs = service.SelectKH();
        dtm.setRowCount(0);
        for (KhachHangViewModels ds : khachHangs) {
            Object row[] = {ds.getId(), ds.getMa(), ds.getTen(), ds.getTenDem(), ds.getHo(), ds.getNgaySinh(), ds.getsDT(), ds.getDiaChi(), ds.getThanhPho(), ds.getQuocGia(), ds.getMatKhau()};
            dtm.addRow(row);
        }

    }

    public KhachHang GetFrom() {
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String dem = txtTenDem.getText();
        String ho = txtHo.getText();
        String ngayS = txtNgaySinh.getText();
        String SDt = txtSdt.getText();
        String diaC = txtDiaChi.getText();
        String thanhP = txtThanhPho.getText();
        String quocGia = txtQuocGia.getText();
        String mk = txtMK.getText();
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd-MM-yyyy");
        try {
            Date date = simpleDateFormat.parse(ngayS);
            java.sql.Date sqldate = new java.sql.Date(date.getTime());
            KhachHang khachHang = new KhachHang(ma, ten, dem, ho, sqldate, SDt, diaC, thanhP, quocGia, mk);
            return khachHang;
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        return null;
    }
