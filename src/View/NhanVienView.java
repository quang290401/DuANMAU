    private void tblNvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNvMouseClicked
        int row = tblNv.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = tblNv.getValueAt(row, 0).toString();
        String ma = tblNv.getValueAt(row, 1).toString();
        String ten = tblNv.getValueAt(row, 2).toString();
        String dem = tblNv.getValueAt(row, 3).toString();
        String ho = tblNv.getValueAt(row, 4).toString();
        String gioiTinh = tblNv.getValueAt(row, 5).toString();
        String ngaySinh = tblNv.getValueAt(row, 6).toString();
        String diaC = tblNv.getValueAt(row, 7).toString();
        String sdt = tblNv.getValueAt(row, 8).toString();
        String matKhau = tblNv.getValueAt(row, 9).toString();

        String trangThai = tblNv.getValueAt(row, 13).toString();

        this.txtID.setText(id);
        this.txtMa.setText(ma);
        this.txtTen.setText(ten);
        this.TxtTenDem.setText(dem);
        this.txtHo.setText(ho);
        this.txtGioiTinh.setText(gioiTinh);
        this.txtNgaySinh.setText(ngaySinh);
        this.txtDiaCHi.setText(diaC);
        this.TxtSDT.setText(sdt);
        this.txtMatKhau.setText(matKhau);
        Object idCHObj = tblNv.getValueAt(row, 10);
        Object idCVObj = tblNv.getValueAt(row, 11);
        Object idBCObj = tblNv.getValueAt(row, 12);

// Kiểm tra và chuyển đổi thành chuỗi
        String idCH = (idCHObj != null) ? idCHObj.toString() : "";
        String idCV = (idCVObj != null) ? idCVObj.toString() : "";
        String idBC = (idBCObj != null) ? idBCObj.toString() : "";

// Đặt mô hình cho combobox CbbIdCh
        DefaultComboBoxModel<String> idChModel = new DefaultComboBoxModel<>();
        idChModel.addElement(idCH);
        CbbIdCh.setModel(idChModel);

// Đặt mô hình cho combobox CbbIdCV
        DefaultComboBoxModel<String> idCvModel = new DefaultComboBoxModel<>();
        idCvModel.addElement(idCV);
        CbbIdCV.setModel(idCvModel);

// Đặt mô hình cho combobox CbbIdBCao
        DefaultComboBoxModel<String> idBcModel = new DefaultComboBoxModel<>();
        idBcModel.addElement(idBC);
        CbbIdBCao.setModel(idBcModel);
        this.txtTrangThai.setText(trangThai);


    }//GEN-LAST:event_tblNvMouseClicked

