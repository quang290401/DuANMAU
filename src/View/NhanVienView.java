    public NhanVien GetFrom() {
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String dem = TxtTenDem.getText();
        String ho = txtHo.getText();
        String gioiTinh = txtGioiTinh.getText();
        String ngaySinh = txtNgaySinh.getText();
        String diaC = txtDiaCHi.getText();
        String sdt = TxtSDT.getText();
        String matKhau = txtMatKhau.getText();
        NhanVienViewModels idbaocao = (NhanVienViewModels) CbbIdBCao.getSelectedItem();
        String idbc = null;
        if (idbaocao != null) {
            idbc = idbaocao.getId();
        }

        CuaHangViewModel idCh = (CuaHangViewModel) this.CbbIdCh.getSelectedItem();
        String idCuahang = null;
        if (idCh != null) {
            idCuahang = idCh.getId();
        }

        ChucVuViewModels idCV = (ChucVuViewModels) CbbIdCV.getSelectedItem();
        String idchucvu = null;
        if (idCV != null) {
            idchucvu = idCV.getId();
        }
        String trangThai = txtTrangThai.getText();
        Integer tt = Integer.valueOf(trangThai);

        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd-MM-yyyy");
        try {
            Date date = simpleDateFormat.parse(ngaySinh);
            java.sql.Date sqldate = new java.sql.Date(date.getTime());
            NhanVien nhanVien = new NhanVien(ma, ten, dem, ho, gioiTinh, sqldate, diaC, sdt, matKhau, idCuahang, idchucvu, idbc, tt);
            return nhanVien;
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        return null;
    }
